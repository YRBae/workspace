package chapter3;

public class LogicalOr {
	public static void main(String[] args) {
		System.out.println("TURE OR TRUE = "+(true || true));
		System.out.println("TURE OR FALSE = "+(true || true));
		System.out.println("FALSE OR TRUE = "+(false || true));
		System.out.println("FALSE OR FLASE = "+(false || false));
	}
}